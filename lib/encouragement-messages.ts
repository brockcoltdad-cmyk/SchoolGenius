// ğŸ“Š HELPER FUNCTIONS
export function getRandomMessage(messages: string[]): string {
  return messages[Math.floor(Math.random() * messages.length)]
}

export function getMessageByScore(percentage: number): string {
  if (percentage === 100) return getRandomMessage(quizResultMessages.perfect)
  if (percentage >= 90) return getRandomMessage(quizResultMessages.excellent)
  if (percentage >= 80) return getRandomMessage(quizResultMessages.good)
  if (percentage >= 70) return getRandomMessage(quizResultMessages.passing)
  return getRandomMessage(quizResultMessages.needsPractice)
}

export function getStreakMessage(streak: number): string {
  if (streak >= 2 && streak <= 10) {
    return streakMessages[streak - 2] || streakMessages[streakMessages.length - 1]
  }
  return getRandomMessage(correctMessages)
}

export function getContextualMessage(
  isCorrect: boolean,
  consecutiveWrong: number,
  timeSpent: number,
  isFirstTime: boolean
): string {
  // First time special
  if (isFirstTime) return getRandomMessage(firstTimeMessages)

  // Correct answer
  if (isCorrect) {
    if (timeSpent < 3000) return getRandomMessage(fastAndCorrectMessages)
    return getRandomMessage(correctMessages)
  }

  // Wrong answer - check if struggling
  if (consecutiveWrong >= 3) {
    return getRandomMessage(strugglingButSupportiveMessages)
  }

  // Regular wrong answer
  return getRandomMessage(wrongButEncouragingMessages)
}
