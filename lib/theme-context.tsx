interface ThemeContextType {
  currentTheme: Theme;
  setTheme: (themeId: ThemeId) => void;
}

const ThemeContext = createContext<ThemeContextType | undefined>(undefined);

export function ThemeProvider({ children }: { children: ReactNode }) {
  const [currentThemeId, setCurrentThemeId] = useState<ThemeId>('default');
  const [isLoaded, setIsLoaded] = useState(false);

  useEffect(() => {
    const loadTheme = async () => {
      const isDemoMode = typeof window !== 'undefined' && localStorage.getItem('demo_mode') === 'true';

      if (!isDemoMode) {
        const childId = localStorage.getItem('current_child_id');

        if (childId) {
          try {
            const { createClient } = await import('@supabase/supabase-js');
            const supabase = createClient(
              process.env.NEXT_PUBLIC_SUPABASE_URL!,
              process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!
            );

            const { data } = await supabase
              .from('children')
              .select('current_theme')
              .eq('id', childId)
              .maybeSingle();

            if (data?.current_theme && themes[data.current_theme as ThemeId]) {
              setCurrentThemeId(data.current_theme as ThemeId);
              setIsLoaded(true);
              return;
            }
          } catch (error) {
            console.error('Error loading theme:', error);
          }
        }
      }

      const savedTheme = localStorage.getItem('schoolgenius-theme') as ThemeId;
      if (savedTheme && themes[savedTheme]) {
        setCurrentThemeId(savedTheme);
      }
      setIsLoaded(true);
    };

    loadTheme();
  }, []);

  const handleSetTheme = async (themeId: ThemeId) => {
    setCurrentThemeId(themeId);
    localStorage.setItem('schoolgenius-theme', themeId);

    const isDemoMode = typeof window !== 'undefined' && localStorage.getItem('demo_mode') === 'true';

    if (!isDemoMode) {
      const childId = localStorage.getItem('current_child_id');
      if (childId) {
        try {
          const { createClient } = await import('@supabase/supabase-js');
          const supabase = createClient(
            process.env.NEXT_PUBLIC_SUPABASE_URL!,
            process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!
          );

          await supabase
            .from('children')
            .update({ current_theme: themeId })
            .eq('id', childId);
        } catch (error) {
          console.error('Error saving theme:', error);
        }
      }
    }
  };

  if (!isLoaded) {
    return <div className="min-h-screen bg-slate-50" />;
  }

  return (
    <ThemeContext.Provider
      value={{
        currentTheme: themes[currentThemeId],
        setTheme: handleSetTheme,
      }}
    >
      {children}
    </ThemeContext.Provider>
  );
}

export function useTheme() {
  const context = useContext(ThemeContext);
  if (context === undefined) {
    throw new Error('useTheme must be used within a ThemeProvider');
  }
  return context;
}
